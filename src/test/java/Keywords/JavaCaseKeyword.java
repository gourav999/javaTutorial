package Keywords;

public class JavaCaseKeyword {
	public static void main(String[] args) {
		String weekday = "Tuesday";

		switch (weekday) {
		case "Monday":
			System.out.println("Monday");
			break;
		case "Tuesday":
			System.out.println("Tuesday");
			break;
		default:
			System.out.println("default");

		}
	}

}
